#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	res;
	int	signe;

	i = 0;
	res = 0;
	signe = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		signe = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit((int)str[i]))
	{
		res += (str[i] - 48);
		i++;
		if (!(ft_isdigit((int)str[i])))
			return (res * signe);
		res *= 10;
	}
	return (res * signe);
}
